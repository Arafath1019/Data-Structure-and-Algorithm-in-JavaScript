// Static vs Dynamic Array:

/*
    Array is mainly in two types:
        1. Static array
        2. Dynamic array

    Static Array: 
        In static array, size of the array is fixed. 
        The content of the array can be modified but without changing the memory space allocated to ir.


    Dynamic Array: 
        In dynamic array, the size of the array is not fixed and can be modified during the operations performed on it.
        Dynamic data structures are designed to facilitate change of data structure in the run time.
         
*/