// Hash Collisions:

/*

     operations -> Big O Notations
    1. insert   -> O(1)
    2. lookup   -> O(1)
    3. delete   -> O(1)
    4. search   -> O(1)



    Hash collision is a feature of hash table, where it occurs, whenever, same index of two data values are inserted into same index, and this is called hash collisions.

    There are a lot of ways to solve this hash collisions, one of them is linked list, 
    and this is the way of making a linked list among the hash collisions data.

    
    Big O Notation of hash collision is O(n/k); where k is the size of the hash table
    simply we can say, O(n)
*/