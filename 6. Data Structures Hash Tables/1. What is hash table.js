//What is hash table?

/*
    Hash table is a data structure which stores data in an associative manner. In a hash table, data is stored in an array format, where each data value has its own unique index value.
    Access of data becomes very fast if we know the index of the desired data.

    Thus, it becomes a data structure where insertion and searching operations are very fast irrespective of the size of the data. 
    Hash table uses an array as a storage medium and uses hash technique to generate an index where an element is to be inserted.

*/


