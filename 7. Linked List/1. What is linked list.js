// What is linked list?

/*
    Linked list is a very commonly used linear data structure, which contains a group of nodes in a sequence.

    Each node holds its own data and the address of the next node hence forming a chain like data structure.

    Linked lists are used to create trees and graphs.

    There are three types of linked list:
        1.  Singly Linked list
        2. Doubly linked list
        . circular linked list

*/

// Why linked list?

/*
    1. Dynamic data structure
    2. Insertion and deletions are really easier.
    3. No-memory wastage
    4. Implementation of stacks and queues can be easily using linked list.
*/

